#include "libft.h"

void	ft_putnbr(int n)
{
	long tmp;

	if (!n)
		write(1, "0", 1);
	else
	{
		tmp = (n < 0) ? -1 : 1;
		while (n)
		{
			tmp = tmp * 10 + n % 10;
			n /= 10;
		}
		if (tmp < 0)
		{
			write(1, "-", 1);
			tmp = ~tmp + 1;
		}
		while (tmp != 1)
		{
			n = tmp % 10 + '0';
			write(1, &n, 1);
			tmp /= 10;
		}
	}
}
